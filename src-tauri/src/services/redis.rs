use super::{hidden_command, kill_tree, posix, Service, ServiceStatus};
use std::fs;
use std::path::PathBuf;
use std::process::Child;

pub const DEFAULT_PORT: u16 = 6379;

pub struct RedisService {
    redis_dir: PathBuf,
    runtime_dir: PathBuf,
    port: u16,
    child: Option<Child>,
}

impl RedisService {
    pub fn new(redis_dir: PathBuf, runtime_dir: PathBuf) -> Self {
        Self {
            redis_dir,
            runtime_dir,
            port: DEFAULT_PORT,
            child: None,
        }
    }

    pub fn set_port(&mut self, port: u16) {
        self.port = port;
    }

    fn ensure_conf(&self) -> Result<PathBuf, String> {
        let conf_dir = self.runtime_dir.join("redis");
        let data_dir = conf_dir.join("data");
        fs::create_dir_all(&data_dir).map_err(|e| e.to_string())?;
        let conf_path = conf_dir.join("redis.conf");
        let log_path = conf_dir.join("redis.log");
        let conf = format!(
            "# Generated by Lamp Bench. Do not edit by hand.\n\
             port {port}\n\
             bind 127.0.0.1\n\
             dir \"{data}\"\n\
             logfile \"{log}\"\n\
             loglevel notice\n\
             appendonly no\n\
             save 900 1\n\
             save 300 10\n",
            port = self.port,
            data = posix(&data_dir),
            log = posix(&log_path),
        );
        fs::write(&conf_path, conf).map_err(|e| e.to_string())?;
        Ok(conf_path)
    }
}

impl Service for RedisService {
    fn start(&mut self) -> Result<(), String> {
        if self.child.is_some() {
            return Ok(());
        }
        let conf = self.ensure_conf()?;
        // tporadowski's Redis-x64 zip drops redis-server.exe at the root.
        let server = self
            .redis_dir
            .join(format!("redis-server{}", std::env::consts::EXE_SUFFIX));
        if !server.exists() {
            return Err(format!("redis-server not found at {}", server.display()));
        }
        let child = hidden_command(&server)
            .arg(&conf)
            .spawn()
            .map_err(|e| format!("failed to spawn redis-server: {e}"))?;
        self.child = Some(child);
        Ok(())
    }

    fn stop(&mut self) -> Result<(), String> {
        if let Some(mut child) = self.child.take() {
            kill_tree(&mut child);
        }
        Ok(())
    }

    fn status(&mut self) -> ServiceStatus {
        match self.child.as_mut() {
            Some(child) => match child.try_wait() {
                Ok(None) => ServiceStatus::Running { pid: child.id() },
                Ok(Some(_)) => {
                    self.child = None;
                    ServiceStatus::Stopped
                }
                Err(e) => ServiceStatus::Error {
                    message: e.to_string(),
                },
            },
            None => ServiceStatus::Stopped,
        }
    }
}
